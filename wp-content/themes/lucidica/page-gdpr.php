						 <h3> How we are preparing for GDPR?</h3>

						 <p> We already have a consistent level of data protection and security across our organisation, however it is our aim to be fully compliant with the GDPR by 25th May 2018. Outside the renewal of contracts with a GDPR clause with our clients, third parties and suppliers, our preparation includes –  </p>
						 <p style="text-align: left; font-size: 17px; padding: 10px;  background-color: #f2f2f2;">
						
						1.Information audit – employees at Lucidica to record PII they handle on a daily basis, where it’s kept, and how they ensure its safety <br>
						2.Data protection – Our CRM running on-premise Sharepoint 2013, with only senior engineers accessing the back-end running SQL. The CRM is encrypted by SSL, and accessed remotely only by the use of IPSEC VPN <br>
						3.Data retention and erasure – the use of EMS to create labels and set retention policies <br>
						4.Supplier transfers – check GDPR compliancy of suppliers, and be transparent with the data subjects on where they data is kept<br>
						5.Subject access requests – double verification for SARs, once confirmed, respond within 30 days in a reasonable format <br>
						6.Transfer of sensitive data – use of EMS to send encrypted e-mails, and create policies to apply document/e-mail expiry
						7.Employee training – Training internal staff, 2 of the staff are GDPR certified<br>
						8. Mobile management – use of EMS to manage mobile devices to ensure security when accessing our systems, and allow remote wipe and pre-requisite checks<br>
						9. Obtaining specific consent when a data subject signs up to our newsletter on our website, and offering a clear opt out on all e-mail marketing<br>
				        10. Review/Create a new clients data policy for retention and destruction of computer
				        </p>

